$(".get-city").change(function(e){
	var state = $(this).val();
	var wrapper = $(this).data('wrapper');
	$.ajax({
        type: "POST",
        url: '<?= base_url('Dashboard/get_city'); ?>',
        data:{state:state},
        success: function(data){
        	$(wrapper).html(data)
        }
    }); 	
})
$(".is_address_same").change(function(e){
	var is_address_same = $(this).val();
	if ($(this).is(":checked")) { 
		$("#address_current").val($("#address_permanent").val());
		$("#address_current").attr('disabled', 'disabled');

		$("#pincode_current").val($("#pincode_permanent").val());
		$("#pincode_current").attr('disabled', 'disabled');

		$("#state_current").val($("#state_permanent").val());
		$("#state_current").attr('disabled', 'disabled');
		$("#state_current").change();
		setTimeout(function(){$("#city_current").val($("#city_permanent").val());
		$("#city_current").attr('disabled', 'disabled');},1000);
		
	}else{
		$("#address_current").val('');
		$("#address_current").removeAttr('disabled');

		$("#pincode_current").val('');
		$("#pincode_current").removeAttr('disabled');

		$("#state_current").val('');
		$("#state_current").removeAttr('disabled');

		$("#city_current").html('');
		$("#city_current").removeAttr('disabled');
	}
})
