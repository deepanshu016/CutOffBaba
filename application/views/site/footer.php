			required: true,
			email:true
		},
		mobile: {
			required: true,
			minlength: 10,
			maxlength:10,
			number:true
		},
		password: "required",
    rptpassword: {
      equalTo: "#password"
    }
	},
	submitHandler: function() { 
		$.ajax({
      type: "POST",
      url: "<?=base_url('newregistration');?>",
      data: $("#registerfrm").serialize(),
      dataType: "json",
      encode: true,
    }).done(function (data) {
    	if (data.status=='success') {
     window.location.href=data.url;
   }else{

   	if (data.errors.email!="") {
   		$('.alert').html(data.errors.email).show();
   	}else if (data.errors.mobile!="") {
   		$('.alert').html(data.errors.mobile).show();
   	}else
   	if (data.errors.name!="") {
   		$('.alert').html(data.errors.name).show();
   	}else
   	if (data.errors.password!="") {
   		$('.alert').html(data.errors.password).show();
   	}
   	
   }
    });

    event.preventDefault();
	}
});
